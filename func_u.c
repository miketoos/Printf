#include "libft_printf.h"

int			func_u(size_t nb, t_list *all)
{
	(void)all;
	if (nb <= 9)
		ft_putchar(nb + '0');
	else if (nb > 9)
	{
		func_u(nb / 10, all);
		func_u(nb % 10, all);
	}
	return (0);
}
