#include "libft_printf.h"

void		ft_putnbr_u(size_t nb)
{
	if (nb <= 9)
		ft_putchar(nb + '0');
	else if (nb > 9)
	{
		ft_putnbr_u(nb / 10);
		ft_putnbr_u(nb % 10);
	}
}
